#include<bits/stdc++.h>
using namespace std;

int main(){
/*	assignment operators
	int x= 10;
	x+=5;//x= x+5
	x-=5;//x= x-5
	x*=5;//x= x*5
	x/=5;//x= x/5
	x%=5;//x= x%5
	cout<< x<< endl; 
*/	


//increment operators
int x=20;
//post increment i.e after assigning the value to variable, the value is incremented 
cout<< x++ << endl;
cout<< x<< endl;
//pre increment
cout<< ++x << endl;
cout<< x<< endl;
//post decrement
cout<< x-- << endl;
cout<< x<< endl;
//pre decrement
cout<< --x << endl;
cout<< x<< endl;
	
	
		
	
	return 0;
}
