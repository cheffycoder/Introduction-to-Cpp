#include <bits/stdc++.h>
using namespace std;

int main(){
	// loop is a certain bit of code that runs over an over again until condition is true
	int bacon=0;
	
	/*
	infinite loop
	while(bacon<=5){
		cout<< "Bacon is " << bacon<< endl;
	}
	*/
	
	while(bacon<=5){
		cout<< "Bacon is " << bacon<< endl;
		bacon+=1;
	}
	
	while(bacon<=5){
		cout<< "condition is true "<< endl;
	}
	
	cout<<"condition is false "<< endl;
	
	return 0;
}



